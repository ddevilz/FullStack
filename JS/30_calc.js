function add(num1, num2){
    let result = num1 + num2;
    return result
}

function subtract(num1, num2){
    let result = num1 - num2;
    return result
}

function multiply(num1, num2){
    let result = num1 * num2;
    return result
}

function divide(num1, num2){
    let result = num1 / num2;
    return result
}

console.log(add(3,2))
console.log(subtract(3,2))
console.log(multiply(3,2))
console.log(divide(3,2))